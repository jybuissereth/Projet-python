def get_nb_empty_rooms(plateau):

	"""
	Compte et renvoie le nombre de cases libres dans le plateau
	"""

	i=0
	count_empty_rooms=0
	while i<len(plateau['tiles']):
		if plateau['tiles'][i]==0:
			count_empty_rooms+=1
		i+=1
	plateau['nombre_cases_libres']=count_empty_rooms  
	return count_empty_rooms


